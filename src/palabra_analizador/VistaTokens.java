        Object g[] = new Object[5];

        for (int i = 0; i < h.size(); i++) {
            tt.addRow(g);
            TokenB getP = (TokenB) h.get(i);
            tt.setValueAt(getP.getToken(), i, 0);
            tt.setValueAt(getP.getCodigo(), i, 1);
            tt.setValueAt(getP.getLinea(), i, 2);
            tt.setValueAt(getP.getColumna(), i, 3);
            tt.setValueAt(getP.getInfo(), i, 4);
        }

